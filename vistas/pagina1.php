		<h2 align="center">Pre debut</h2>
		<p align="justify">
		En 2011 la joven banda comenzó a formarse, y rápidamente comenzaron a sumar 
		seguidores en redes sociales, con el tiempo subieron algunos covers en Youtube 
		y SoundCloud que fueron muy populares. Ese mismo año lanzaron su primera canción
		«Paldogangsan». Desde el sus inicios Big Hit, CEO de Bang Sihyuk supo que esta banda
		tendría un gran futuro, por eso decidieron apostar fuerte.​
		</p>
		
		<h2 align="center">Debut oficial</h2>
		<p align="justify">
		El debut oficial fue el 13 de junio de 2013 con la canción “No More Dream”. Desde ese 
		momento el grupo comenzó a afianzarse, y fue por eso que incluyeron el tema en el álbum
		“O!RUL8,2?” el cual se promocionó en los principales programas de música. El siguiente 
		año, realizaron una presentación en M Countdown, y desde ese instante su fama no paró de
		crecer, y algunos miembros del grupo fueron llamados para participar en el vídeo musical
		«I’m Da One» de Jo Kwon del grupo 2AM.
		
		</p>
		
		<h2 align="center">Primeras giras</h2>
		
		<p align="justify">
		El 12 de febrero de 2014 lanzaron al mercado “Skool Luv Affair” que finalizaría la trilogía
		de la escuela. Este disco llegó a encabezar la Gaon Album Chart, y vendió 100.000 copias. 
		Su canción “No More Dream”, llegó a ocupar el puesto número 4 de la Billboard Japón Top 
		Single Sales, y el número 6 de la lista Oricon Daily Chart. Se vendieron más de 32.000 unidades 
		sólo en la primera semana. No tardaron el llegar los viajes para la joven band, primero el Festival
		de Cultura Coreana de Moscú y luego a Estados Unidos para grabar su nuevo álbum de estudio.
		Aprovechando su estadía participaron en el reality American Hustle Lifeel en Los Angeles, el
		cual comenzó a emitirse en julio en la cadena Mnet.
		</p>
		
		
